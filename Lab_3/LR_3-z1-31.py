def cout(label: str, collection, extra=None):
    """Выводит метку и элементы коллекции.

    Аргументы:
        label (str): Метка для вывода.
        collection (iterable): Коллекция элементов, которые нужно вывести.
        extra (any, optional): Дополнительный аргумент для вывода. По умолчанию None.
    """
    output = f'{label}: ' + ', '.join(map(str, collection))

    if extra is not None:
        output = f"{label} '{extra}': " + ', '.join(map(str, collection))

    print(output)


def main():
    """Основная функция программы.

    Инициализирует список четных чисел, копирует его, выполняет
    операции замены и удаления элементов, а также вычисляет сумму
    квадратов оставшихся элементов.
    """
    # Инициализация списка A с четными числами
    A = [i * 2 for i in range(20)]
    cout("Список A", A)

    # Копирование списка A в B
    B = A.copy()
    cout("Список B", B)

    # Замена четных по индексу элементов A на их индексы
    for i in range(0, len(A), 2):
        A[i] = i
    cout("Список A после замены четных по индексу элементов на их индекс", A)

    # Проверка наличия элемента 122 в списке A
    if 122 in A:
        print("Элемент 122 остался в списке A")
    else:
        print("Элемент 122 отсутствует в списке A")

    # Удаление первых и последних двух элементов из списка B
    B = B[2:-2]
    cout("Список B после удаления первых и последних двух элементов", B)

    # Вычисление суммы квадратов всех элементов списка B
    sum_squares = sum(x ** 2 for x in B)
    print("Сумма квадратов всех элементов списка B:", sum_squares)


if __name__ == "__main__":
    main()
